const useStyles = makeStyles((theme) => ({
    paper: {
        marginTop: theme.spacing(8),
        display: 'flex',
        flexDirection: 'column',
        alignItems: 'center',
    },
    avatar: {
        margin: theme.spacing(1),
        backgroundColor: theme.palette.secondary.main,
    },
    form: {
        width: '100%', // Fix IE 11 issue.
        marginTop: theme.spacing(1),
    },
    submit: {
        margin: theme.spacing(3, 0, 2),
    },
}));

export default function SignIn(props) {
    const [typedUsername,setTypedUsername] = useState('');
    const [typedPassword,setTypedPassword] = useState('');
    let history = useHistory();
    const classes = useStyles();
    const handleLoginClick = (e) => {
        e.preventDefault();
        const auth = firebase.auth();
        const db = firebase.firestore();



        auth.signInWithEmailAndPassword(typedUsername, typedPassword)
            .then(auth.onAuthStateChanged(function(user) {
            if (user) {
                history.push("/changepath");
               let name = user.displayName;
               let email = user.email;
                console.log(email +'userlogged');
            } else {
                console.log("wrong email or password")
                console.log('fail');
            }
        }))


    }

    const handleNameChange = (event) => {
        setTypedUsername(event.target.value)
    }

    const handlePasswordChange = (event) => {
        setTypedPassword(event.target.value)
    }

    return (
        <Container component="main" maxWidth="xs">
            <CssBaseline />
            <div className={classes.paper}>
                <Avatar className={classes.avatar}>
                    <LockOutlinedIcon />
                </Avatar>
                <Typography component="h1" variant="h5">
                    Sign in
                </Typography>
                <form className={classes.form} noValidate onSubmit={handleLoginClick} >
                    <TextField
                        onChange={handleNameChange}
                        variant="outlined"
                        margin="normal"
                        required
                        fullWidth
                        id="name"
                        label="Name"
                        name="name"
                        autoComplete="name"
                        autoFocus
                    />
                    <TextField
                        onChange={handlePasswordChange}
                        variant="outlined"
                        margin="normal"
                        required
                        fullWidth
                        name="password"
                        label="Password"
                        type="password"
                        id="password"
                        autoComplete="current-password"
                    />
                    <FormControlLabel
                        control={<Checkbox value="remember" color="primary" />}
                        label="Remember me"
                    />
                    <Button
                        type="submit"
                        fullWidth
                        variant="contained"
                        color="primary"
                        className={classes.submit}
                    >
                        Sign In
                    </Button>
                    <Grid container>
                        <Grid item xs>
                            <Link href="#" variant="body2">
                                Forgot password?
                            </Link>
                        </Grid>
                        <Grid item>
                            <Link href="/#/register" variant="body2">
                                {"Don't have an account? Sign Up"}
                            </Link>
                            <Link href="/#/" variant="body2">
                                {" Go back"}
                            </Link>
                        </Grid>
                    </Grid>
                </form>
            </div>
            <Box mt={8}>
                <Copyright />
            </Box>
        </Container>
    );
}